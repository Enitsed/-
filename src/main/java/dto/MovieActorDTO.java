package dto;

public class MovieActorDTO {
	private int movie_actor_num;
	private int movie_num;
	private int actor_num;
	
	public int getMovie_actor_num() {
		return movie_actor_num;
	}
	public void setMovie_actor_num(int movie_actor_num) {
		this.movie_actor_num = movie_actor_num;
	}
	public int getMovie_num() {
		return movie_num;
	}
	public void setMovie_num(int movie_num) {
		this.movie_num = movie_num;
	}
	public int getActor_num() {
		return actor_num;
	}
	public void setActor_num(int actor_num) {
		this.actor_num = actor_num;
	}
}
